<h2>What is Hanacaraka ?</h2>

<p>Hanacaraka, also known as Carakan or Surakarta Script, is an abugida writing system used for writing Javanese language. It was developed in the 14th century during the Majapahit empire, which ruled over Java and other parts of Indonesia from 1293 to 1520.

The earliest known example of Hanacaraka is the Sukuraman manuscript, which dates back to 1350. The script was used for writing religious texts, court documents, and literary works, and it quickly became popular among Javanese writers and scholars.

Hanacaraka continued to be used for centuries, but its popularity declined in the 20th century as Indonesia adopted the Latin alphabet as its official writing system. Today, the script is mainly used for ceremonial purposes, such as inscriptions on traditional Javanese buildings and monuments, and in traditional Javanese dance and theater.

Efforts have been made to revive Hanacaraka and promote its use among younger generations of Javanese speakers. In 2009, the Indonesian government recognized Hanacaraka as a national cultural heritage, and it is now taught in schools and universities as part of Javanese language and culture studies.</p>

